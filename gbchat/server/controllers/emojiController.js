// @desc    Get all emoji categories
// @route   GET /api/emoji/categories
// @access  Private
export const getEmojiCategories = asyncHandler(async (req, res) => {
  const categories = Object.values(EMOJI_CATEGORIES).map(cat => ({
    id: cat.id,
    name: cat.name,
    emojiCount: cat.emojis.length,
  }));

  res.json({
    success: true,
    data: categories,
  });
});

// @desc    Get emojis by category
// @route   GET /api/emoji/category/:categoryId
// @access  Private
export const getEmojisByCategory = asyncHandler(async (req, res) => {
  const { categoryId } = req.params;
  
  const category = Object.values(EMOJI_CATEGORIES).find(
    cat => cat.id === categoryId
  );

  if (!category) {
    return res.status(404).json({
      success: false,
      message: 'Category not found',
    });
  }

  res.json({
    success: true,
    data: {
      id: category.id,
      name: category.name,
      emojis: category.emojis,
    },
  });
});

