// @desc    Search emojis
// @route   GET /api/emoji/search
// @access  Private
export const searchEmojis = asyncHandler(async (req, res) => {
  const { q } = req.query;

  if (!q) {
    return res.status(400).json({
      success: false,
      message: 'Search query is required',
    });
  }

  const searchLower = q.toLowerCase();
  const results = {};

  Object.entries(EMOJI_CATEGORIES).forEach(([key, category]) => {
    // Search in category name
    if (category.name.toLowerCase().includes(searchLower)) {
      results[key] = category.emojis;
      return;
    }

    // For now, return all emojis if search matches
    // In a real app, you'd have emoji names/keywords to search
    if (searchLower.length === 1) {
      results[key] = category.emojis;
    }
  });

  res.json({
    success: true,
    data: results,
  });
});

