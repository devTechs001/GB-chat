// @desc    Get emoji by unicode
// @route   GET /api/emoji/unicode/:unicode
// @access  Private
export const getEmojiByUnicode = asyncHandler(async (req, res) => {
  const { unicode } = req.params;

  // Search through all categories
  for (const category of Object.values(EMOJI_CATEGORIES)) {
    if (category.emojis.includes(unicode)) {
      return res.json({
        success: true,
        data: {
          emoji: unicode,
          category: category.name,
        },
      });
    }
  }

  res.status(404).json({
    success: false,
    message: 'Emoji not found',
  });
});
